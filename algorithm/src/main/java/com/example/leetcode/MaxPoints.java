package com.example.leetcode;

/**
 * Created by Wilber
 * on 2017-12-01 .
 * Given n points on a 2D plane,
 * find the maximum number of points that lie on the same straight line.
 */

public class MaxPoints {
    class Point {
        int x;
        int y;

        Point() {
            x = 0;
            y = 0;
        }

        Point(int a, int b) {
            x = a;
            y = b;
        }
    }
}
